public class TestInstanceof {

	public static void main(String[] args) {
		
		String name = "Sayed";

		boolean result = name instanceof String; 
		System.out.println( result );
	}
}

/*
Kode di atas digunakan untuk mencoba operator Instanceof,
operator ini digunakan untuk membandingkan suatu objek apakah objek tersebut termasuk dari suatu class atau tidak,
dan dari perbandingan tersebut akan menghasilkan nilai boolean berupa nilai true/false.
*/