public class TestSwitchExample{

	public static void main(String[] args){
		int number = 20;

		switch(number){
			case 10: System.out.println("10");break;
			case 20: System.out.println("20");break;
			case 30: System.out.println("30");break;
			default:System.out.println("Not in 10, 20 or 30");  
  

  }  
 }  
}   
 
 /*
Kode di atas digunakan untuk mencoba menyeleksi switch-case untuk mengecek
case hingga menemukan case yang bersifat true, Jika semua case tersebut telah diseleksi
dan tidak terdapat kondisi yang bersifat true maka akan dilanjutkan ke default
*/