public class TestSwitchExample2{

	public static void main(String[] args){
		int number=20;

		switch(number){
			case 10: System.out.println("10");  
  			case 20: System.out.println("20");  
  			case 30: System.out.println("30");  
  			default:System.out.println("Not in 10, 20 or 30");  
  
  }  
 }  
} 

/*
Kode di atas digunakan untuk mengetahui perbedaan dengan menggunakan break,
perbedaannya yaitu apabila case terpenuhi maka semua hasil akan ditampilkan dimulai dari case yang terpenuhi tersebut
*/