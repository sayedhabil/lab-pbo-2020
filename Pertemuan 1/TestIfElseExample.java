public class TestIfElseExample {

	public static void main(String[] args) {
		int number = 13;
		if(number % 2 == 0){
			System.out.println("Angka genap");
		} else{
			System.out.println("Angka ganjil");
		}
	}
}

/*
Kode di atas digunakan untuk mencoba menyeleksi if-else pada suatu kondisi,
jika kondisi tersebut true atau terpenuhi maka akan diteruskan ke pernyataan, jika suatu kondisi tidak terpenuhi
atau false maka dilanjutkan ke else yang tidak ada kondisi.
*/